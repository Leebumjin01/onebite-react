let animal = "cat";

switch (animal) {
    case "cat":
        {
            console.log("고양이");
            break;
        }
    case "dog":
        {
            console.log("강아지");
            break;
        }
    case "bear":
        {
            console.log("곰");
            break;
        }
    case "tiger":
        {
            console.log("호랑이");
            break;
        }
    default: {
        console.log("그런 동물은 모릅니다.");
    }
}